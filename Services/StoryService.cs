        public static List<DialoguePart> GetVillagerDialogue(string className)
        {
            var dialogueList = new List<DialoguePart>();

            // 1. ชาวบ้านพูด (ฝั่งขวา)
            dialogueList.Add(new DialoguePart
            {
                SpeakerName = "ชาวบ้าน (คุกเข่าอ้อนวอน)",
                Side = "Right",
                Message = "ท่านผู้กล้า... ในที่สุดสวรรค์ก็เมตตา พวกท่านมาเพื่อปลดปล่อยอาณาจักรนี้จากฝันร้ายใช่ไหม? จอมมารพรากทุกอย่างไปจากเรา... ได้โปรดเถอะ มอบความหวังให้พวกเราเห็นสักครั้ง..."
            });

            // 2. ตัวละครตอบ (ฝั่งซ้าย) - แบ่งเป็นท่อนๆ ให้กดอ่านทีละคลิก
            string speaker = "";
            string[] messages = Array.Empty<string>();

            switch (className)
            {
                case "Knight":
                    speaker = "อัศวินผู้ร่วงหล่น";
                    messages = new string[]
                    {
                "หุบปากซะ... อย่าได้ริอาจเรียกข้าด้วยชื่ออันทรงเกียรติเช่นนั้น ข้าไม่ได้มาที่นี่เพื่อช่วยใคร และไม่ได้มาเพื่อกอบกู้แผ่นดินที่เน่าเฟะนี่",
                "ข้ามาเพื่อชดใช้ในสิ่งที่ข้าเคยก่อ... \nเลือดของจอมมารคือสิ่งเดียวที่จะล้างคราบคาวของการทรยศออกจากมือข้าได้ ต่อให้ต้องเหยียบย่ำซากศพของพวกเจ้าทุกคนเพื่อให้ได้มาซึ่งการไถ่บาป... ข้าก็จะทำ",
                    };
                    break;

                case "Berserker":
                    speaker = "นักรบคลั่ง";
                    messages = new string[]
                    {
                "เฮอะ! ความหวังงั้นเหรอ? กินมันให้อิ่มท้องได้ไหมล่ะ? \nข้าไม่ได้เดินทางฝ่าฝูงปีศาจมาเพื่อฟังเสียงคร่ำครวญน่ารำคาญของพวกเจ้า",
                "ข้ามาเพื่อ 'จอก' นั่นเพียงสิ่งเดียว ใครก็ตามที่ขวางทางข้า... ไม่ว่าจะเป็นสมุนของจอมมาร หรือชาวบ้านกระจอกๆ อย่างพวกเจ้า ข้าจะสับให้แหลกไปพร้อมกันทั้งหมด! ถ้าอยากตายอย่างสงบ ก็ไสหัวไปให้พ้นทางข้าซะ!",
                    };
                    break;

                case "Wizard":
                    speaker = "จอมเวทนอกรีต";
                    messages = new string[]
                    {
                "น่าเวทนาเสียจริง... พวกเจ้ายังเชื่อในนิทานหลอกเด็กเรื่องผู้ช่วยให้รอดอยู่อีกเหรอ? ดูสภาพโลกใบนี้สิ มันพังทลายลงเพราะความโง่เขลาของพวกเจ้านั่นแหละ",
                "สำหรับข้า... พวกเจ้าก็เป็นแค่เครื่องสังเวยที่ไร้ค่าตัวหนึ่ง ข้าไม่ได้สนใจว่าอาณาจักรนี้จะรุ่งเรืองหรือล่มสลาย",
                "ข้าแค่ต้องการดึงเอาความลับสูงสุดของจักรวาลออกมาจากหัวของจอมมาร... \nและถ้าการเห็นพวกเจ้าทุกข์ทรมานมันทำให้ข้าเข้าถึงเวทมนตร์ที่แข็งแกร่งขึ้นได้ ข้าก็ยินดีจะยืนดูพวกเจ้ามอดไหม้ไปกับตา",
                    };
                    break;
            }

            foreach (var msg in messages)
            {
                dialogueList.Add(new DialoguePart { SpeakerName = speaker, Side = "Left", Message = msg });
            }

            return dialogueList;
        }

